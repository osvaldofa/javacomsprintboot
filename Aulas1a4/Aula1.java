package Aulas1a4;

/*
 * Nesse exemplo vamos aprender a estrutura mais básica de um programa em Java.
 * Class é a palavra reservada para definir uma classe.
 * Aula1 é o nome da classe.
 * public é um modificador de acesso que define que a classe é pública.
 * static é uma palavra reservada que define que o método main é estático.
 * void é uma palavra reservada que define que o método main não retorna nada.
 * main é o nome do método principal.
 * String[] args é um array de strings que são os argumentos passados para o programa.
 */
public class Aula1 {
    public static void main(String[] args) {
        System.out.println("Olá, Mundo!");
    }
}